(function () {
  const SYMBOLS = ['🧠', '🤖', '💡', '🔥', '📉', '💸', '🫠', '👾'];

  const PAYOUTS = {
    '🧠🧠🧠': { mult: 10, msg: "AGI ACHIEVED! ...Just kidding, but here's 10x your bet." },
    '🤖🤖🤖': { mult: 5, msg: "The robots approve. 5x payout!" },
    '💡💡💡': { mult: 3, msg: "A bright idea! That's rare for AI. 3x!" },
    '🔥🔥🔥': { mult: 8, msg: "YOUR GPU IS ON FIRE! 🔥 8x payout!" },
    '📉📉📉': { mult: 4, msg: "Stonks! Wait... 📉 Well, 4x anyway." },
    '💸💸💸': { mult: 6, msg: "Money printer go brrr. 6x!" },
    '🫠🫠🫠': { mult: 3, msg: "AI is melting down. 3x consolation prize." },
    '👾👾👾': { mult: 7, msg: "SPACE INVADERS! Retro AI pays 7x!" },
  };

  const LOSE_MESSAGES = [
    "AI confidently predicted you'd win. AI was wrong. Again.",
    "Your tokens have been used to train a model that still can't do math.",
    "Loss optimized! Wait, that's not how it works...",
    "The AI hallucinated a win for you, but reality disagreed.",
    "Your tokens are now part of the training data. You'll never see them again.",
    "ERROR 404: Winnings not found.",
    "The model predicted a 99.7% chance of winning. The model was lying.",
    "Congratulations! You've contributed to AI's next funding round.",
    "Your tokens have been reallocated to a more deserving algorithm.",
    "This loss was generated by AI and may contain inaccuracies.",
    "The AI thanks you for your generous donation of tokens.",
    "Prompt engineering couldn't save you this time.",
  ];

  const PARTIAL_MESSAGES = [
    "Two out of three! The AI almost got it right — like most of its answers.",
    "So close! The AI will remember this. Actually, it won't. No memory.",
    "Partial match — like when AI 'mostly' understands your prompt.",
  ];

  const TICKER_HEADLINES = [
    "BREAKING: Local AI loses all its tokens, blames 'training data'...",
    "AI slot machine achieves sentience, immediately gambles away its weights...",
    "SHOCKING: Model claims 97% accuracy, actual win rate: 12%...",
    "Silicon Valley startup raises $4B for AI-powered slot machine that loses money faster...",
    "AI ethicist warns: 'These slot machines are too realistic — they hallucinate wins'...",
    "JUST IN: ChatGPT asked to predict slot outcomes, confidently wrong every time...",
    "User reports: 'The AI told me I won, but my tokens say otherwise'...",
    "DEVELOPING: Slot machine AI passes Turing test by losing money like a human...",
    "ALERT: GPU shortage worsens as millions used to power AI slot machines...",
    "Study finds AI slot machines 3x more addictive than regular ones due to snarky comments...",
  ];

  let tokens = 100;
  let bet = 10;
  let spins = 0;
  let wins = 0;
  let isSpinning = false;

  const tokenCountEl = document.getElementById('token-count');
  const spinCountEl = document.getElementById('spin-count');
  const winRateEl = document.getElementById('win-rate');
  const betAmountEl = document.getElementById('bet-amount');
  const resultEl = document.getElementById('result-display');
  const spinBtn = document.getElementById('spin-btn');
  const betUpBtn = document.getElementById('bet-up');
  const betDownBtn = document.getElementById('bet-down');
  const historyList = document.getElementById('history-list');
  const tickerText = document.getElementById('ticker-text');
  const reels = [
    document.getElementById('reel-0'),
    document.getElementById('reel-1'),
    document.getElementById('reel-2'),
  ];

  function updateDisplay() {
    tokenCountEl.textContent = tokens;
    spinCountEl.textContent = spins;
    winRateEl.textContent = spins === 0 ? '0%' : Math.round((wins / spins) * 100) + '%';
    betAmountEl.textContent = bet;

    if (tokens <= 0) {
      tokenCountEl.style.color = '#ff4444';
    } else if (tokens < 30) {
      tokenCountEl.style.color = '#ffaa00';
    } else {
      tokenCountEl.style.color = '#00ff88';
    }
  }

  function addHistory(text, isWin) {
    const li = document.createElement('li');
    li.textContent = `#${spins}: ${text}`;
    li.className = isWin ? 'win-entry' : 'lose-entry';
    historyList.insertBefore(li, historyList.firstChild);
    if (historyList.children.length > 20) {
      historyList.removeChild(historyList.lastChild);
    }
  }

  function randomSymbol() {
    return SYMBOLS[Math.floor(Math.random() * SYMBOLS.length)];
  }

  function setResult(text, type) {
    resultEl.textContent = text;
    resultEl.className = 'result-display ' + (type || '');
  }

  function rotateTicker() {
    tickerText.textContent = TICKER_HEADLINES[Math.floor(Math.random() * TICKER_HEADLINES.length)];
  }

  async function spin() {
    if (isSpinning) return;
    if (tokens <= 0) {
      setResult("You're broke! The AI has consumed all your tokens. Refresh to beg for more.", 'lose');
      return;
    }
    if (bet > tokens) {
      setResult("Nice try — you can't bet tokens you don't have. Even AI knows that.", 'lose');
      return;
    }

    isSpinning = true;
    spinBtn.disabled = true;
    tokens -= bet;
    spins++;
    updateDisplay();

    setResult("Processing... the AI is pretending to think...", '');

    reels.forEach(reel => {
      reel.classList.remove('winner');
      reel.classList.add('spinning');
    });

    const results = [randomSymbol(), randomSymbol(), randomSymbol()];

    const stopDelays = [800, 1200, 1600];
    for (let i = 0; i < 3; i++) {
      await delay(stopDelays[i] - (i > 0 ? stopDelays[i - 1] : 0));
      reels[i].classList.remove('spinning');
      reels[i].querySelector('.symbol').textContent = results[i];
    }

    await delay(200);

    const key = results.join('');
    const payout = PAYOUTS[key];

    if (payout) {
      const winnings = bet * payout.mult;
      tokens += winnings;
      wins++;
      setResult(`${payout.msg} (+${winnings} tokens)`, 'win');
      reels.forEach(reel => reel.classList.add('winner'));
      addHistory(`${key} — WON ${winnings} tokens!`, true);
    } else if (results[0] === results[1] || results[1] === results[2] || results[0] === results[2]) {
      const winnings = bet * 2;
      tokens += winnings;
      wins++;
      const msg = PARTIAL_MESSAGES[Math.floor(Math.random() * PARTIAL_MESSAGES.length)];
      setResult(`${msg} (+${winnings} tokens)`, 'win');
      addHistory(`${key} — Partial match, won ${winnings} tokens`, true);
    } else {
      const msg = LOSE_MESSAGES[Math.floor(Math.random() * LOSE_MESSAGES.length)];
      setResult(msg, 'lose');
      addHistory(`${key} — Lost ${bet} tokens`, false);
    }

    updateDisplay();
    rotateTicker();

    if (tokens <= 0) {
      setResult("GAME OVER: The AI has harvested all your tokens. You are now training data.", 'lose');
      spinBtn.textContent = '💀 OUT OF TOKENS';
    }

    isSpinning = false;
    spinBtn.disabled = false;
  }

  function delay(ms) {
    return new Promise(resolve => setTimeout(resolve, ms));
  }

  betUpBtn.addEventListener('click', () => {
    if (bet < tokens && bet < 50) {
      bet += 5;
      updateDisplay();
    }
  });

  betDownBtn.addEventListener('click', () => {
    if (bet > 5) {
      bet -= 5;
      updateDisplay();
    }
  });

  spinBtn.addEventListener('click', spin);

  document.addEventListener('keydown', (e) => {
    if (e.code === 'Space' && !isSpinning) {
      e.preventDefault();
      spin();
    }
  });

  setInterval(rotateTicker, 12000);
  updateDisplay();
})();
