(() => {
  "use strict";

  // ── Symbols & Weights ──────────────────────────────
  // Lower weight = rarer
  const SYMBOLS = [
    { emoji: "🤖", name: "Robot",   weight: 8  },
    { emoji: "🧠", name: "Brain",   weight: 6  },
    { emoji: "💎", name: "Diamond", weight: 5  },
    { emoji: "🔥", name: "Fire",    weight: 10 },
    { emoji: "☁️", name: "Cloud",   weight: 12 },
    { emoji: "⚡", name: "Bolt",    weight: 12 },
    { emoji: "🐛", name: "Bug",     weight: 15 },
  ];

  // Payout multipliers for three-of-a-kind
  const PAYOUTS = {
    "🤖": 50,
    "🧠": 25,
    "💎": 20,
    "🔥": 15,
    "☁️": 10,
    "⚡": 10,
    "🐛": 5,
  };

  const PARTIAL_MATCH_MULT = 2; // any two matching

  // ── AI Roast Messages ─────────────────────────────
  const LOSE_MESSAGES = [
    "Your tokens vanished into the cloud. Literally.",
    "AI confidently predicted you'd win. It was hallucinating.",
    "Training complete. Result: you lost.",
    "GPT says: 'I'm sorry to hear about your tokens.'",
    "Your tokens were used to fine-tune a model that still can't count.",
    "Tokens sacrificed to the machine learning gods.",
    "The AI looked at your luck and said 'insufficient data.'",
    "Your tokens are now part of a training dataset.",
    "Error 402: Payment accepted, winnings not found.",
    "The neural network says: better luck next epoch.",
    "Your tokens were lost in a gradient descent.",
    "The AI suggested you stop. But it's also wrong about everything.",
    "Tokens consumed. Carbon footprint increased. No refunds.",
    "This loss was generated by AI. So it might not be real. (It is.)",
    "Model confidence: 99%. Accuracy: 0%. Tokens: gone.",
  ];

  const WIN_MESSAGES = [
    "The AI accidentally paid you instead of OpenAI's server bill!",
    "You won! Don't tell the VCs.",
    "Congratulations! Your tokens were hallucinated into existence!",
    "The model overfitted to your luck!",
    "Winner! Quick, spend them before the next API price hike!",
    "You beat the machine! (It's still smarter than you though.)",
    "Tokens deposited! This result may not be reproducible.",
    "The RNG smiled upon you. The AI did not.",
    "You won tokens! Side effect: your GPU fans are screaming.",
    "Payout confirmed. The AI is seething.",
  ];

  const JACKPOT_MESSAGES = [
    "AGI ACHIEVED! Just kidding, but you did win big!",
    "THREE ROBOTS! The singularity is here and it pays well!",
    "JACKPOT! You've out-earned an AI startup's entire Series A!",
    "MASSIVE WIN! Even Sam Altman is jealous!",
    "THE MACHINES HAVE ALIGNED... in your favor!",
  ];

  const BROKE_MESSAGES = [
    "Your token balance hit zero. Just like most AI startups.",
    "Out of tokens! The AI has consumed everything. As predicted.",
    "Bankrupt! You've successfully simulated an AI company's burn rate.",
    "No more tokens. Maybe try prompt engineering a loan?",
    "Game over. Your tokens were deprecated without notice.",
  ];

  const SPIN_TAUNTS = [
    "Processing your regret...",
    "Consulting the neural oracle...",
    "Running inference on your wallet...",
    "Calculating optimal disappointment...",
    "Feeding tokens to the algorithm...",
    "Loading sarcasm model...",
    "Burning GPU cycles for your amusement...",
  ];

  // ── State ─────────────────────────────────────────
  let tokens = 100;
  let bet = 10;
  let spinning = false;
  const BET_STEP = 5;
  const MIN_BET = 5;

  // ── DOM refs ──────────────────────────────────────
  const tokenCountEl = document.getElementById("token-count");
  const betAmountEl = document.getElementById("bet-amount");
  const betDisplayEl = document.getElementById("bet-display");
  const messageEl = document.getElementById("message");
  const spinBtn = document.getElementById("spin-btn");
  const betUpBtn = document.getElementById("bet-up");
  const betDownBtn = document.getElementById("bet-down");
  const reelsFrame = document.querySelector(".reels-frame");
  const machine = document.querySelector(".slot-machine");
  const reelEls = [
    document.getElementById("reel-0"),
    document.getElementById("reel-1"),
    document.getElementById("reel-2"),
  ];

  // ── Weighted random symbol picker ─────────────────
  const totalWeight = SYMBOLS.reduce((s, sym) => s + sym.weight, 0);

  function pickSymbol() {
    let r = Math.random() * totalWeight;
    for (const sym of SYMBOLS) {
      r -= sym.weight;
      if (r <= 0) return sym.emoji;
    }
    return SYMBOLS[SYMBOLS.length - 1].emoji;
  }

  function randomFrom(arr) {
    return arr[Math.floor(Math.random() * arr.length)];
  }

  // ── Build a strip of random symbols for animation ─
  function buildStrip(finalSymbol, count) {
    const symbols = [];
    for (let i = 0; i < count; i++) {
      symbols.push(pickSymbol());
    }
    symbols.push(finalSymbol); // last one is the result
    return symbols;
  }

  // ── Render a reel strip into DOM ──────────────────
  function renderStrip(reelIndex, symbols) {
    const strip = reelEls[reelIndex].querySelector(".reel-strip");
    strip.innerHTML = "";
    for (const sym of symbols) {
      const div = document.createElement("div");
      div.className = "symbol";
      div.textContent = sym;
      strip.appendChild(div);
    }
    // Reset position to top
    strip.style.transition = "none";
    strip.style.transform = "translateY(0)";
  }

  // ── Animate a single reel ─────────────────────────
  function animateReel(reelIndex, symbols, duration) {
    return new Promise((resolve) => {
      const strip = reelEls[reelIndex].querySelector(".reel-strip");
      const reel = reelEls[reelIndex];
      const symbolHeight = reel.offsetHeight; // each symbol = reel height
      const totalDistance = (symbols.length - 1) * symbolHeight;

      reel.classList.add("spinning");

      // Force reflow then animate
      requestAnimationFrame(() => {
        strip.style.transition = `transform ${duration}ms cubic-bezier(0.15, 0.8, 0.3, 1)`;
        strip.style.transform = `translateY(-${totalDistance}px)`;
      });

      setTimeout(() => {
        reel.classList.remove("spinning");
        resolve();
      }, duration);
    });
  }

  // ── Calculate winnings ────────────────────────────
  function calculateResult(results) {
    const [a, b, c] = results;

    // Three of a kind
    if (a === b && b === c) {
      const mult = PAYOUTS[a] || 5;
      return { type: a === "🤖" ? "jackpot" : "win", multiplier: mult };
    }

    // Two of a kind
    if (a === b || b === c || a === c) {
      return { type: "partial", multiplier: PARTIAL_MATCH_MULT };
    }

    // Nothing
    return { type: "lose", multiplier: 0 };
  }

  // ── Update UI ─────────────────────────────────────
  function updateTokenDisplay() {
    tokenCountEl.textContent = tokens;
    tokenCountEl.classList.add("bump");
    setTimeout(() => tokenCountEl.classList.remove("bump"), 200);
  }

  function updateBetDisplay() {
    betAmountEl.textContent = bet;
    betDisplayEl.textContent = bet;
  }

  function setMessage(text, type) {
    messageEl.textContent = text;
    messageEl.className = "";
    if (type) messageEl.classList.add(type);
  }

  // ── Init reels with starting symbols ──────────────
  function initReels() {
    for (let i = 0; i < 3; i++) {
      renderStrip(i, [pickSymbol()]);
    }
  }

  // ── SPIN ──────────────────────────────────────────
  async function spin() {
    if (spinning) return;
    if (tokens < bet) {
      setMessage(randomFrom(BROKE_MESSAGES), "broke");
      machine.classList.add("shake");
      setTimeout(() => machine.classList.remove("shake"), 400);
      return;
    }

    spinning = true;
    spinBtn.disabled = true;

    // Deduct bet
    tokens -= bet;
    updateTokenDisplay();
    setMessage(randomFrom(SPIN_TAUNTS), "");

    // Pick final results
    const results = [pickSymbol(), pickSymbol(), pickSymbol()];

    // Build strips (more symbols = longer visual spin)
    const stripCounts = [18, 24, 30]; // staggered stops
    const durations = [1200, 1600, 2000];

    for (let i = 0; i < 3; i++) {
      const strip = buildStrip(results[i], stripCounts[i]);
      renderStrip(i, strip);
    }

    // Small delay then animate all reels
    await new Promise((r) => setTimeout(r, 50));

    const animations = reelEls.map((_, i) =>
      animateReel(i, buildStrip(results[i], stripCounts[i]), durations[i])
    );
    await Promise.all(animations);

    // Evaluate
    const result = calculateResult(results);
    const winnings = result.multiplier * bet;

    // Clear previous flash classes
    reelsFrame.classList.remove("win-flash", "jackpot-flash");
    machine.classList.remove("shake");

    if (result.type === "jackpot") {
      tokens += winnings;
      updateTokenDisplay();
      setMessage(`${randomFrom(JACKPOT_MESSAGES)} +${winnings} tokens!`, "jackpot");
      reelsFrame.classList.add("jackpot-flash");
    } else if (result.type === "win") {
      tokens += winnings;
      updateTokenDisplay();
      setMessage(`${randomFrom(WIN_MESSAGES)} +${winnings} tokens!`, "win");
      reelsFrame.classList.add("win-flash");
    } else if (result.type === "partial") {
      tokens += winnings;
      updateTokenDisplay();
      setMessage(`Partial match! +${winnings} tokens. ${randomFrom(WIN_MESSAGES)}`, "win");
      reelsFrame.classList.add("win-flash");
    } else {
      setMessage(randomFrom(LOSE_MESSAGES), "lose");
      machine.classList.add("shake");
      setTimeout(() => machine.classList.remove("shake"), 400);
    }

    // Check if broke
    if (tokens <= 0) {
      tokens = 0;
      updateTokenDisplay();
      setTimeout(() => {
        setMessage(randomFrom(BROKE_MESSAGES) + " Here's 50 pity tokens.", "broke");
        tokens = 50;
        updateTokenDisplay();
      }, 1500);
    }

    // Clamp bet to available tokens
    if (bet > tokens && tokens > 0) {
      bet = Math.max(MIN_BET, Math.floor(tokens / BET_STEP) * BET_STEP);
      if (bet > tokens) bet = MIN_BET;
      updateBetDisplay();
    }

    spinning = false;
    spinBtn.disabled = false;
  }

  // ── Bet controls ──────────────────────────────────
  betUpBtn.addEventListener("click", () => {
    if (spinning) return;
    const newBet = bet + BET_STEP;
    if (newBet <= tokens) {
      bet = newBet;
      updateBetDisplay();
    }
  });

  betDownBtn.addEventListener("click", () => {
    if (spinning) return;
    const newBet = bet - BET_STEP;
    if (newBet >= MIN_BET) {
      bet = newBet;
      updateBetDisplay();
    }
  });

  // ── Spin triggers ─────────────────────────────────
  spinBtn.addEventListener("click", spin);

  document.addEventListener("keydown", (e) => {
    if (e.code === "Space" && !spinning) {
      e.preventDefault();
      spin();
    }
  });

  // ── Init ──────────────────────────────────────────
  initReels();
  updateBetDisplay();
  updateTokenDisplay();
})();
